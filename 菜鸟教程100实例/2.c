/*
题目：企业发放的奖金根据利润提成。

利润(I)低于或等于10万元时，奖金可提10%；
利润高于10万元，低于20万元时，低于10万元的部分按10%提成，高于10万元的部分，可提成7.5%；
20万到40万之间时，高于20万元的部分，可提成5%；
40万到60万之间时高于40万元的部分，可提成3%；
60万到100万之间时，高于60万元的部分，可提成1.5%；
高于100万元时，超过100万元的部分按1%提成。
从键盘输入当月利润I，求应发放奖金总数？

程序分析：请利用数轴来分界，定位。注意定义时需把奖金定义成长整型
*/
#include <stdio.h>
int main(){
    int i;
    double j;
    printf("请输入利润，你这个奸商\n");
    scanf("%d",&i);
    if(i<=100000){
        j = i * 0.1;
    }else if(i<=200000){
        j = 100000*0.1 + (i - 100000) * 0.075;
    }else if(i<=400000){
        j = 100000*0.1 + 100000*0.075 + (i-200000) * 0.05;
    }else if(i<=600000){
        j = 100000*0.1 + 100000*0.075 + 200000*0.05 + (i-400000) * 0.03;
    }else if(i<=10000000){
        j = 100000*0.1 + 100000*0.075 + 200000*0.05 + 200000*0.03 + (i-600000) * 0.015;
    }else{
        j = 100000*0.1 + 100000*0.075 + 200000*0.05 + 200000*0.03 + 400000*0.15 + (i-10000000) * 0.01;
    }
    printf("你的奖金是 %.2lf\n", j);
    return 0;
}
